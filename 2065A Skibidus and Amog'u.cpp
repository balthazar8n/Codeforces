#include <bits/stdc++.h>
using namespace std;

void solve() {
    string W;
    cin >> W;

    for (int i = 0; i < W.size() - 2; i++) {
        cout << W[i] << "";
    }
    cout << "i" << endl;
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}
